const {
  solve,
  isSolved,
  prettyBoard,
} = require('../sudoku.js');

describe('isSolved', () => {
  it('No duplicates in small squares', () => {
    expect(isSolved('123456789456789123789123456214365897365897214897214365531642978642978531978531642')).toBe(true);
    expect(isSolved('123456789456789123789123456214365897365897214897214365531642978642978531978531644')).toBe(false);
  });

  it('No empty cells', () => {
    expect(isSolved('123456798457198236869237145214365987375981462698724351531679824946812573782543619')).toBe(true);
    expect(isSolved('1-58-2----9--764-52--4--819-19--73-6762-83-9-----61-5---76---3-43--2-5-16--3-89--')).toBe(false);
    expect(isSolved('1234567984571982368692371452143659873759814626987243515316798249468125737-2543619')).toBe(false);
  });

  it('No duplicates in columns', () => {
    expect(isSolved('145892673893176425276435819519247386762583194384961752957614238438729561621358947')).toBe(true);
    expect(isSolved('1-58-2----9--764-52--4--819-19--73-6762-83-9-----61-5---76---3-43--2-5-16--3-89--')).toBe(false);
    expect(isSolved('145892673193176425276435819519247386762583194384961752957614238438729561621358947')).toBe(false);
  });
});

describe('solve', () => {
  it('Solves easy puzzles', () => {
    expect(solve('1-58-2----9--764-52--4--819-19--73-6762-83-9-----61-5---76---3-43--2-5-16--3-89--')).toBe('145892673893176425276435819519247386762583194384961752957614238438729561621358947');
    expect(solve('--5-3--819-285--6-6----4-5---74-283-34976---5--83--49-15--87--2-9----6---26-495-3')).toBe('475936281932851764681274359517492836349768125268315497153687942794523618826149573');
  });
});
